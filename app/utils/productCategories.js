export function normalizeProductCategory(category) {
  const value = String(category || "").trim();

  if (!value) {
    return "";
  }

  if (PRODUCT_CATEGORY_SET.has(value)) {
    return value;
  }

  return PRODUCT_CATEGORY_ALIASES.get(value) || "";
}
